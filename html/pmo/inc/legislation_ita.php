                <div class="row-fluid">
                    <h4>ARRESTO, FERMATA E SOSTA ART. 157 CDS</h4>

                    <ul>
                        <li>
                            1. Agli effetti delle presenti norme:

                            per arresto si intende l'interruzione della marcia del veicolo dovuta ad esigenze della
                            circolazione;
                            per fermata si intende la temporanea sospensione della marcia anche se in area ove non sia
                            ammessa la sosta, per consentire la salita o la discesa delle persone, ovvero per altre
                            esigenze di brevissima durata. Durante la fermata, che non deve comunque arrecare intralcio
                            alla circolazione, il conducente deve essere presente e pronto a riprendere la marcia;
                            per sosta si intende la sospensione della marcia del veicolo protratta nel tempo, con
                            possibilità di allontanamento da parte del conducente;
                            per sosta di emergenza si intende l'interruzione della marcia nel caso in cui il veicolo è
                            inutilizzabile per avaria ovvero deve arrestarsi per malessere fisico del conducente o di un
                            passeggero.
                        </li>
                        <li>
                            2. Salvo diversa segnalazione, ovvero nel caso previsto dal comma 4, in caso di fermata o di
                            sosta il veicolo deve essere collocato il più vicino possibile al margine destro della
                            carreggiata, parallelamente ad esso e secondo il senso di marcia. Qualora non esista
                            marciapiede rialzato, deve essere lasciato uno spazio sufficiente per il transito dei
                            pedoni, comunque non inferiore ad un metro. Durante la sosta, il veicolo deve avere il
                            motore spento.
                        </li>
                        <li>
                            3. Fuori dei centri abitati, i veicoli in sosta o in fermata devono essere collocati fuori
                            della carreggiata, ma non sulle piste per velocipedi né, salvo che sia appositamente
                            segnalato, sulle banchine. In caso di impossibilità, la fermata e la sosta devono essere
                            effettuate il più vicino possibile al margine destro della carreggiata, parallelamente ad
                            esso e secondo il senso di marcia. Sulle carreggiate delle strade con precedenza la sosta è
                            vietata.
                        </li>
                        <li>
                            4. Nelle strade urbane a senso unico di marcia la sosta è consentita anche lungo il margine
                            sinistro della carreggiata, purché rimanga spazio sufficiente al transito almeno di una fila
                            di veicoli e comunque non inferiore a tre metri di larghezza.
                        </li>
                        <li>
                            5. Nelle zone di sosta all'uopo predisposte i veicoli devono essere collocati nel modo
                            prescritto dalla segnaletica.
                        </li>
                        <li>
                            6. Nei luoghi ove la sosta è permessa per un tempo limitato è fatto obbligo ai conducenti di
                            segnalare, in modo chiaramente visibile, l'orario in cui la sosta ha avuto inizio. Ove
                            esiste il dispositivo di controllo della durata della sosta è fatto obbligo di porlo in
                            funzione.
                        </li>
                        <li>
                            7. È fatto divieto a chiunque di aprire le porte di un veicolo, di discendere dallo stesso,
                            nonché di lasciare aperte le porte, senza essersi assicurato che ciò non costituisca
                            pericolo o intralcio per gli altri utenti della strada.
                        </li>
                        <li>
                            7-bis. È fatto divieto di tenere il motore acceso, durante la sosta o fermata del veicolo,
                            allo scopo di mantenere in funzione l'impianto di condizionamento d'aria nel veicolo stesso
                        </li>
                        <li>
                            8. Chiunque viola le disposizioni di cui al presente articolo è soggetto alla sanzione
                            amministrativa
                        </li>
                    </ul>
                </div>
