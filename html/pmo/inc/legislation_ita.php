                    <h4>MODALITA' COMUNICAZIONE DATI EX ART. 126 BIS CDS</h4>
                    Ai sensi dell'art. 126 bis C.d.S, la violazione comporta a carico del trasgressore la decurtazione
                    di punti dalla patente di guida, che varia a seconda del tipo di infrazione contestata, e verrà
                    annotata presso un apposito Registro di M.C.T.C. Italiana.
                    A tal fine il proprietario del veicolo dovrà inviare a questo organo di polizia entro 60 gg dalla
                    notifica del presente verbale il nominativo ed i dati identificativi della patente di guida del
                    conducente del proprio veicolo.
                    In caso contrario sarà applicata un ulteriore sanzione ex art 126 bis C.d.S che va da un minimo di
                    Euro 286,00 ad un massimo di Euro 1.142,00.
                </div>
