                <div class="row-fluid">
                    <h4>DIVIETO DI FERMATA E SOSTA ART. 158 CDS</h4>

                    <ul>
                        <li>
                            1. La fermata e la sosta sono vietate:

                            in corrispondenza o in prossimità dei passaggi a livello e sui binari di linee ferroviarie o
                            tranviarie o così vicino ad essi da intralciarne la marcia;
                            nelle gallerie, nei sottovia, sotto i sovrapassaggi, sotto i fornici e i portici, salvo
                            diversa segnalazione;
                            sui dossi e nelle curve e, fuori dei centri abitati e sulle strade urbane di scorrimento,
                            anche in loro prossimità;
                            in prossimità e in corrispondenza di segnali stradali verticali e semaforici in modo da
                            occultarne la vista, nonché in corrispondenza dei segnali orizzontali di preselezione e
                            lungo le corsie di canalizzazione;
                            fuori dei centri abitati, sulla corrispondenza e in prossimità delle aree di intersezione;
                            nei centri abitati, sulla corrispondenza delle aree di intersezione e in prossimità delle
                            stesse a meno di 5 m dal prolungamento del bordo più vicino della carreggiata trasversale,
                            salvo diversa segnalazione;
                            sui passaggi e attraversamenti pedonali e sui passaggi per ciclisti, nonché sulle piste
                            ciclabili e agli sbocchi delle medesime;
                            sui marciapiedi, salvo diversa segnalazione.
                        </li>
                        <li>
                            2. La sosta di un veicolo è inoltre vietata:

                            allo sbocco dei passi carrabili;
                            dovunque venga impedito di accedere ad un altro veicolo regolarmente in sosta, oppure lo
                            spostamento di veicoli in sosta * ;
                            in seconda fila, salvo che si tratti di veicoli a due ruote, due ciclomotori a due ruote o
                            due motocicli * ;
                            negli spazi riservati allo stazionamento e alla fermata degli autobus, dei filobus e dei
                            veicoli circolanti su rotaia e, ove questi non siano delimitati, a una distanza dal segnale
                            di fermata inferiore a 15 m, nonché negli spazi riservati allo stazionamento dei veicoli in
                            servizio di piazza * ;
                            <div class="PointMinus">PUNTI DECURTATI DALLA PATENTE DI GUIDA: 2</div>
                            sulle aree destinate al mercato e ai veicoli per il carico e lo scarico di cose, nelle ore
                            stabilite;
                            sulle banchine, salvo diversa segnalazione;
                            negli spazi riservati alla fermata o alla sosta dei veicoli per persone invalide di cui
                            all'art. 188 e in corrispondenza degli scivoli o dei raccordi tra i marciapiedi, rampe o
                            corridoi di transito e la carreggiata utilizzati dagli stessi veicoli;
                            <div class="PointMinus">PUNTI DECURTATI DALLA PATENTE DI GUIDA: 2</div>
                            nelle corsie o carreggiate riservate ai mezzi pubblici;
                            <div class="PointMinus">PUNTI DECURTATI DALLA PATENTE DI GUIDA: 2</div>
                            nelle aree pedonali urbane;
                            nelle zone a traffico limitato per i veicoli non autorizzati;
                            negli spazi asserviti ad impianti o attrezzature destinate a servizi di emergenza o di
                            igiene pubblica indicati dalla apposita segnaletica;
                            davanti ai cassonetti dei rifiuti urbani o contenitori analoghi;
                            limitatamente alle ore di esercizio, in corrispondenza dei distributori di carburante
                            ubicati sulla sede stradale ed in loro prossimità sino a 5 m prima e dopo le installazioni
                            destinate all'erogazione.
                        </li>
                        <li>
                            3. Nei centri abitati è vietata la sosta dei rimorchi quando siano staccati dal veicolo
                            trainante, salvo diversa segnalazione.
                        </li>
                        <li>
                            4. Durante la sosta e la fermata il conducente deve adottare le opportune cautele atte a
                            evitare incidenti ed impedire l'uso del veicolo senza il suo consenso.
                        </li>
                        <li>
                            5. Chiunque viola le disposizioni del comma 1 e delle lettere d), g) e h) del comma 2 è
                            soggetto alla sanzione amministrativa
                        </li>
                        <li>
                            6. Chiunque viola le altre disposizioni del presente articolo è soggetto alla sanzione
                            amministrativa
                        </li>
                        <li>
                            7. Le sanzioni di cui al presente articolo si applicano per ciascun giorno di calendario per
                            il quale si protrae la violazione.
                        </li>
                    </ul>
                    * La competenza a disporre la rimozione dei veicoli, in questi casi, può anche essere conferita agli
                    "ausiliari del traffico"
                </div>
