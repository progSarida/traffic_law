                    <h4> REGOLAMENTAZIONE DELLA CIRCOLAZIONE NEI CENTRI ABITATI ART. 7 CDS</h4>

                    <ul>
                        <li>
                            1. Nei centri abitati i comuni possono, con ordinanza del sindaco:

                            adottare i provvedimenti indicati nell'art. 6, commi 1, 2 e 4;
                            limitare la circolazione di tutte o di alcune categorie di veicoli per accertate e motivate
                            esigenze di prevenzione degli inquinamenti e di tutela del patrimonio artistico, ambientale
                            e naturale, conformemente alle direttive impartite dal Ministro delle infrastrutture e dei
                            trasporti, sentiti, per le rispettive competenze, il Ministro dell'ambiente e della tutela
                            del territorio ed il Ministro per i beni culturali e ambientali;
                            stabilire la precedenza su determinate strade o tratti di strade, ovvero in una determinata
                            intersezione, in relazione alla classificazione di cui all'art. 2, e, quando la intensità o
                            la sicurezza del traffico lo richiedano, prescrivere ai conducenti, prima di immettersi su
                            una determinata strada, l'obbligo di arrestarsi all'intersezione e di dare la precedenza a
                            chi circola su quest'ultima;
                            riservare limitati spazi alla sosta dei veicoli degli organi di polizia stradale di cui
                            all'art. 12, dei vigili del fuoco, dei servizi di soccorso, nonché di quelli adibiti al
                            servizio di persone con limitata o impedita capacità motoria, munite del contrassegno
                            speciale, ovvero a servizi di linea per lo stazionamento ai capilinea;
                            stabilire aree nelle quali è autorizzato il parcheggio dei veicoli;
                            stabilire, previa deliberazione della giunta, aree destinate al parcheggio sulle quali la
                            sosta dei veicoli è subordinata al pagamento di una somma da riscuotere mediante dispositivi
                            di controllo di durata della sosta, anche senza custodia del veicolo, fissando le relative
                            condizioni e tariffe in conformità alle direttive del Ministero delle infrastrutture e dei
                            trasporti, [di concerto con la Presidenza del Consiglio dei Ministri - Dipartimento per le
                            aree urbane]; (1)
                            prescrivere orari e riservare spazi per i veicoli utilizzati per il carico e lo scarico di
                            cose;
                            istituire le aree attrezzate riservate alla sosta e al parcheggio delle autocaravan di cui
                            all'art. 185;
                            riservare strade alla circolazione dei veicoli adibiti a servizi pubblici di trasporto, al
                            fine di favorire la mobilità urbana.
                            I divieti di sosta si intendono imposti dalle ore 8 alle ore 20, salvo che sia diversamente
                            indicato nel relativo segnale.
                        </li>
                        <li>
                            3. Per i tratti di strade non comunali che attraversano centri abitati, i provvedimenti
                            indicati nell'art. 6, commi 1 e 2, sono di competenza del prefetto e quelli indicati nello
                            stesso articolo, comma 4, lettera a), sono di competenza dell'ente proprietario della
                            strada. I provvedimenti indicati nello stesso comma 4, lettere b), c), d), e) ed f) sono di
                            competenza del comune, che li adotta sentito il parere dell'ente proprietario della strada.
                        </li>
                        <li>
                            4. Nel caso di sospensione della circolazione per motivi di sicurezza pubblica o di
                            sicurezza della circolazione o per esigenze di carattere militare, ovvero laddove siano
                            stati stabiliti obblighi, divieti o limitazioni di carattere temporaneo o permanente,
                            possono essere accordati, per accertate necessità, permessi subordinati a speciali
                            condizioni e cautele. Nei casi in cui sia stata vietata o limitata la sosta, possono essere
                            accordati permessi subordinati a speciali condizioni e cautele ai veicoli riservati a
                            servizi di polizia e a quelli utilizzati dagli esercenti la professione sanitaria,
                            nell'espletamento delle proprie mansioni, nonché dalle persone con limitata o impedita
                            capacità motoria, muniti del contrassegno speciale.
                        </li>
                        <li>
                            5. Le caratteristiche, le modalità costruttive, la procedura di omologazione e i criteri di
                            installazione e di manutenzione dei dispositivi di controllo di durata della sosta sono
                            stabiliti con decreto del Ministro delle infrastrutture e dei trasporti.
                        </li>
                        <li>
                            6. Le aree destinate al parcheggio devono essere ubicate fuori della carreggiata e comunque
                            in modo che i veicoli parcheggiati non ostacolino lo scorrimento del traffico.
                        </li>
                        <li>
                            7. I proventi dei parcheggi a pagamento, in quanto spettanti agli enti proprietari della
                            strada, sono destinati alla installazione, costruzione e gestione di parcheggi in
                            superficie, sopraelevati o sotterranei, e al loro miglioramento e le somme eventualmente
                            eccedenti ad interventi per migliorare la mobilità urbana.
                        </li>
                        <li>
                            8. Qualora il comune assuma l'esercizio diretto del parcheggio con custodia o lo dia in
                            concessione ovvero disponga l'installazione dei dispositivi di controllo di durata della
                            sosta di cui al comma 1, lettera f), su parte della stessa area o su altra parte nelle
                            immediate vicinanze, deve riservare una adeguata area destinata a parcheggio rispettivamente
                            senza custodia o senza dispositivi di controllo di durata della sosta. Tale obbligo non
                            sussiste per le zone definite a norma dell'art. 3 "area pedonale" e "zona a traffico
                            limitato", nonché per quelle definite "A" dall'art. 2 del decreto del Ministro dei lavori
                            pubblici 2 aprile 1968, n. 1444, pubblicato nella Gazzetta Ufficiale n. 97 del 16 aprile
                            1968, e in altre zone di particolare rilevanza urbanistica, opportunamente individuate e
                            delimitate dalla giunta nelle quali sussistano esigenze e condizioni particolari di
                            traffico.
                        </li>
                        <li>
                            9. I comuni, con deliberazione della giunta, provvedono a delimitare le aree pedonali e le
                            zone a traffico limitato tenendo conto degli effetti del traffico sulla sicurezza della
                            circolazione, sulla salute, sull'ordine pubblico, sul patrimonio ambientale e culturale e
                            sul territorio. In caso di urgenza il provvedimento potrà essere adottato con ordinanza del
                            sindaco, ancorché di modifica o integrazione della deliberazione della giunta. Analogamente
                            i comuni provvedono a delimitare altre zone di rilevanza urbanistica nelle quali sussistono
                            esigenze particolari di traffico, di cui al secondo periodo del comma 8. I comuni possono
                            subordinare l'ingresso o la circolazione dei veicoli a motore, all'interno delle zone a
                            traffico limitato, anche al pagamento di una somma. Con direttiva emanata dall'Ispettorato
                            generale per la circolazione e la sicurezza stradale entro un anno dall'entrata in vigore
                            del presente codice, sono individuate le tipologie dei comuni che possono avvalersi di tale
                            facoltà, nonché le modalità di riscossione del pagamento e le categorie dei veicoli
                            esentati.
                        </li>
                        <li>
                            10. Le zone di cui ai commi 8 e 9 sono indicate mediante appositi segnali.
                        </li>
                        <li>
                            11. Nell'ambito delle zone di cui ai commi 8 e 9 e delle altre zone di particolare rilevanza
                            urbanistica nelle quali sussistono condizioni ed esigenze analoghe a quelle previste nei
                            medesimi commi, i comuni hanno facoltà di riservare, con ordinanza del sindaco, superfici o
                            spazi di sosta per veicoli privati dei soli residenti nella zona, a titolo gratuito od
                            oneroso.
                        </li>
                        <li>
                            12. Per le città metropolitane le competenze della giunta e del sindaco previste dal
                            presente articolo sono esercitate rispettivamente dalla giunta metropolitana e dal sindaco
                            metropolitano.
                        </li>
                        <li>
                            13. Chiunque non ottemperi ai provvedimenti di sospensione o divieto della circolazione, è
                            soggetto alla sanzione amministrativa.
                        </li>
                        <li>
                            13-bis. Chiunque, in violazione delle limitazioni previste ai sensi della lettera b) del
                            comma 1, circola con veicoli appartenenti, relativamente alle emissioni inquinanti, a
                            categorie inferiori a quelle prescritte, è soggetto alla sanzione amministrativa e, nel caso
                            di reiterazione della violazione nel biennio, alla sanzione amministrativa accessoria della
                            sospensione della patente di guida da quindici a trenta giorni ai sensi delle norme di cui
                            al capo I, sezione II, del titolo VI.
                        </li>
                        <li>
                            14. Chiunque viola gli altri obblighi, divieti o limitazioni previsti nel presente articolo,
                            è soggetto alla sanzione amministrativa. La violazione del divieto di circolazione nelle
                            corsie riservate ai mezzi pubblici di trasporto, nelle aree pedonali e nelle zone a traffico
                            limitato è soggetta alla sanzione amministrativa.
                        </li>
                        <li>
                            15. Nei casi di sosta vietata, in cui la violazione si prolunghi oltre le ventiquattro ore,
                            la sanzione amministrativa pecuniaria è applicata per ogni periodo di ventiquattro ore, per
                            il quale si protrae la violazione. Se si tratta di sosta limitata o regolamentata, la
                            sanzione amministrativa è applicata per ogni periodo per il quale si protrae la violazione.
                        </li>
                        <li>
                            15-bis. Salvo che il fatto costituisca reato, coloro che esercitano abusivamente, anche
                            avvalendosi di altre persone, ovvero determinano altri ad esercitare abusivamente l'attività
                            di parcheggiatore o guardiamacchine sono puniti con la sanzione amministrativa. Se
                            nell'attività sono impiegati minori la somma è raddoppiata. Si applica, in ogni caso, la
                            sanzione accessoria della confisca delle somme percepite, secondo le norme del capo I,
                            sezione II, del titolo VI.
                        </li>
                    </ul>
